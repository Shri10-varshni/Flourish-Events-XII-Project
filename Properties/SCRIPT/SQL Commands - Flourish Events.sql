#DATABASE CREATION
CREATE DATABASE EVENT_MANAGER;
USE EVENT_MANAGER;

#CONSTANT TABLES - DATA OF AVAILABLE SERVICES
CREATE TABLE EVENT_TYPES (EVENT_TYPEID INT(4) PRIMARY KEY, EVENT_TYPE VARCHAR(64), EVENT_TYPE_DESCRIPTION VARCHAR(1024), BASE_COST INT(8));
CREATE TABLE LOCATIONS (LOCATION_ID INT(4) PRIMARY KEY, EVENT_TYPEID INT(4),LOCATION_TYPE VARCHAR(32), LOCATION_NAME VARCHAR(128), LOCATION_ADDRESS VARCHAR(1024));
CREATE TABLE ADD_ONS (ADDON_TYPEID INT(4) , ADDON_TYPE VARCHAR(64), ADDON_SUBTYPE VARCHAR(64), ADDON_DESCRIPTION VARCHAR(1024), ADDON_COST INT(8));

#USER DEFINED TABLES - INFORMATIONS ABOUT THE USER AND THEIR EVENTS
CREATE TABLE USER_INFORMATION (USER_ID INT(8) PRIMARY KEY NOT NULL auto_incremenT, FIRST_NAME VARCHAR(64), LAST_NAME VARCHAR(64), EMAIL_ID VARCHAR(128), USER_ADDRESS VARCHAR (1024), CONTACT_NUMBER CHAR(16), EVENT_COUNT INT(8) DEFAULT 0, CREATED_DATE DATETIME default current_timestamp, UPDATED_DATE DATETIME default current_timestamp)auto_increment=10001;
CREATE TABLE USER_REGISTRY (USER_ID INT(8) PRIMARY KEY NOT NULL , USER_NAME VARCHAR(128), PASSWORD CHAR(64), CREATED_DATE DATETIME default current_timestamp, UPDATED_DATE DATETIME default current_timestamp);
CREATE TABLE EVENT_INFORMATION (EVENT_ID INT(8) PRIMARY KEY NOT NULL auto_incremenT, USER_ID INT(8), EVENT_TYPEID INT(4), EVENT_NAME VARCHAR(128), START_DATE DATETIME, END_DATE DATETIME , LOCATION_ID INT(4), ATTENDEE_COUNT INT(8), ADD_ONS INT(1), ESTIMATED_COST INT(10), EVENT_DESCRIPTION VARCHAR(1024), EVENT_STATUS INT(1), REGISTER_TIME DATETIME default current_timestamp) auto_increment=1001;
CREATE TABLE EVENT_ADDONS_INFORMATION (EVENT_ID INT(4) , ADDON_ID INT(4), ADDON_COUNT INT(8), ADDON_COST INT(8));

